def sha_key(s: str) -> str:
    h = hashlib.sha256(s.encode("utf-8")).digest()
    return "s." + "".join(f"{b:02x}" for b in h[:8])


def main():
    with open(EN_FILE, encoding="utf-8") as f:
        en_data = json.load(f)
    with open(ZH_FILE, encoding="utf-8") as f:
        zh_data = json.load(f)

    added_en = 0
    added_zh = 0
    updated_zh = 0

    for en_name, zh_name in TRANSLATIONS.items():
        key = sha_key(en_name)

        if key not in en_data:
            en_data[key] = en_name
            added_en += 1

        if key not in zh_data or zh_data[key] == en_data.get(key, en_name):
            if key in zh_data:
                updated_zh += 1
            else:
                added_zh += 1
            zh_data[key] = zh_name

    with open(EN_FILE, "w", encoding="utf-8") as f:
        json.dump(en_data, f, ensure_ascii=False, indent="\t")
        f.write("\n")
    with open(ZH_FILE, "w", encoding="utf-8") as f:
        json.dump(zh_data, f, ensure_ascii=False, indent="\t")
        f.write("\n")

    print(f"EN:  {added_en} new keys added")
    print(f"ZH:  {added_zh} new translations added, {updated_zh} existing stubs replaced")
    print("Done. Merge complete.")


if __name__ == "__main__":
    main()
